import Foundation

extension UIImage {
    
    static func imageNamedUpArrow() -> UIImage {
        return UIImage(named: "greenarrow")!
    }
    
    static func imageNamedDownArrow() -> UIImage {
        return UIImage(named: "redarrow")!
    }
    
}
